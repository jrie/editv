void inc_cursor_x() {

    if (cursor_pos >= STR_END(str) - 1) {
        return;
    }

    cursor_pos++;
}
void dec_cursor_x() {


    if (cursor_pos == 0) {
        return;
    }

    cursor_pos--;
}

